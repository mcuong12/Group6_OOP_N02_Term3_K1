public class SwitchCake {
    public static void switchCase() {
        int choice = 2;
        switch (choice) {
            case 1:
                System.out.println("Chọn bánh số 1");
                break;
            case 2:
                System.out.println("Chọn bánh số 2");
                break;
            default:
                System.out.println("Không có bánh nào cả");
        }
    }
}