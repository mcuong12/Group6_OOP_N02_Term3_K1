public class CakeCase {
    public static void cake(int number) {
        if (number == 1) {
            System.out.println("Bánh kem loại 1");
        } else if (number == 2) {
            System.out.println("Bánh kem loại 2");
        } else {
            System.out.println("Không có loại bánh này");
        }
    }
}